package methodsUnitsConversion;

public class UnitConversion {

    double metreToCent(double met) {
        return met * 100;
    }

    double metreToMm(double met) {
        return met * 1000;
    }

    double cmToMetre(double cm) {
        return cm / 100;
    }

    double mmToMetre(double mil) {
        return mil / 1000;
    }
}
